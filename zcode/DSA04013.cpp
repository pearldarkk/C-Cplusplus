int n, cnt;

int solve() {
    cin >> n;
    v.resize(n);
    for (int i = 0; i < n; ++i)
        cin >> v[i];
    cnt = n;
    sort(v.begin(), v.end());

    for (int i = n/2-1, j = n - 1; i >= 0 && j >= n/2;)
        if (v[j] >= 2 * v[i]) {
            --cnt;
            --j;
            --i;
        }
        else --i;
    return cnt;
}

